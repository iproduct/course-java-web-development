package org.iproduct.rest.exception;

public class NonExistngEntityException extends Exception {

	public NonExistngEntityException() {
		super();
	}

	public NonExistngEntityException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NonExistngEntityException(String arg0) {
		super(arg0);
	}

	public NonExistngEntityException(Throwable arg0) {
		super(arg0);
	}

}
