package users.exceptions;

public class ActionUnsuccessfulException extends Exception {

	public ActionUnsuccessfulException() {
	}

	public ActionUnsuccessfulException(String arg0) {
		super(arg0);
	}

	public ActionUnsuccessfulException(Throwable arg0) {
		super(arg0);
	}

	public ActionUnsuccessfulException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ActionUnsuccessfulException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
